import java.awt.Graphics;

public abstract class GameObject { 
	
	private float x, y;

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
	
	abstract void draw(Graphics g);

}
